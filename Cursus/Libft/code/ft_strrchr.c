char	*ft_strrchr(const char *s, int c)
{
	char			*p;
	unsigned long	i;

	i = 0;
	p = NULL;
	while (*(s + i) != '\0')
	{
		if (*(s + i) == c)
			p = s + i;
		i++;
	}
	if (c = '\0')
		p = s + i;
	return (p);
}

